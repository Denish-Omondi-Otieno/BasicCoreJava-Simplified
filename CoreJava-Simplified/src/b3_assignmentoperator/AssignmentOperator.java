package b3_assignmentoperator;

public class AssignmentOperator {
	
	public static void main (String [] args) {
		int x = 5;
		int y = 6;
		int a = 7;
		int b = 8;
		int c = 9;
		
		x += 5; 
		//x = x + 5
		System.out.println(x);
		
		y -= 6; 
		//y = y - 6
		System.out.println(y);
		
		a /= 7; 
		//a = a/7
		System.out.println(a);
		
		b *= 2; //b = b * 2
		System.out.println(b);
		
		c %= 4; //c = c % 4
		System.out.println(c);
		
		
		
		
		
	}

}
