package b4_ternaryoperator;

public class TernaryOperator {
	
	public static void main (String [] args) {
		//The ternary operator evaluates to either 1 or 0 in logic
		
		boolean x;
		x = (2>7)? true:false;
		System.out.println(x);
		
		int y = 11;
		y = (11==y)?1:0;
		System.out.println(y);
	}

}
