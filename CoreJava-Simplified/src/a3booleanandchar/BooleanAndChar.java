package a3booleanandchar;

public class BooleanAndChar {

	public static void main (String [] args) {
		boolean var = true; // Stores values true or false only
		System.out.println(var);

		char var2 = '@'; // It stores in single quotes
		// can be used to print any character from the unicode table or Keyboard.
		System.out.println(var2);
	}

}
