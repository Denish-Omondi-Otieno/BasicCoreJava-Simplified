package b2lessgreaterthan;

public class LessGreaterThan {
	public static void main (String [] args) {
		int x = 9, y = 8;
		int a = 10, b = 11;
		//Play with the numbers to see the outputs
		
		if (x > y)
			System.out.println("x is greater than y");
		
		if (x >= y)
			System.out.println("x is greater or equal to y");
		
		if (a < b)
			System.out.println("a is less than b");
		
		if (a <= b)
			System.out.println("a is less or equal to b");
	}

}
